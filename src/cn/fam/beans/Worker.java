package cn.fam.beans;

import java.io.Serializable;
import java.sql.Date;

//个人信息
public class Worker implements Serializable {
	private int id;
	private String RegisterName;// 登录名
	private String Password;// 密码
	private String RealName;// 真是姓名
	private Date BirthDate;// 出生日期
	private String Sex;// 性别
	private String Nation;// 名族
	private String WorkSpace;// 工作地点
	private String IdCare;// 身份证号
	private String Education;// 教育程度
	private String Politica;// 政治面貌
	private String Phone;// 手机号码
	private String Type;// 用户类型
	private Date ApplyDate;//申报年份
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getRegisterName() {
		return RegisterName;
	}

	public void setRegisterName(String registerName) {
		RegisterName = registerName;
	}

	public String getPassword() {
		return Password;
	}

	public void setPassword(String password) {
		Password = password;
	}

	public String getRealName() {
		return RealName;
	}

	public void setRealName(String realName) {
		RealName = realName;
	}

	public Date getBirthDate() {
		return BirthDate;
	}

	public void setBirthDate(Date birthDate) {
		BirthDate = birthDate;
	}

	public String getSex() {
		return Sex;
	}

	public void setSex(String sex) {
		Sex = sex;
	}

	public String getNation() {
		return Nation;
	}

	public void setNation(String nation) {
		Nation = nation;
	}

	public String getWorkSpace() {
		return WorkSpace;
	}

	public void setWorkSpace(String workSpace) {
		WorkSpace = workSpace;
	}

	public String getIdCare() {
		return IdCare;
	}

	public void setIdCare(String idCare) {
		IdCare = idCare;
	}

	public String getEducation() {
		return Education;
	}

	public void setEducation(String education) {
		Education = education;
	}

	public String getPolitica() {
		return Politica;
	}

	public void setPolitica(String politica) {
		Politica = politica;
	}

	public String getPhone() {
		return Phone;
	}

	public void setPhone(String phone) {
		Phone = phone;
	}

	public String getType() {
		return Type;
	}

	public void setType(String type) {
		Type = type;
	}

	public Date getApplyDate() {
		return ApplyDate;
	}

	public void setApplyDate(Date applyDate) {
		ApplyDate = applyDate;
	}
	
}
